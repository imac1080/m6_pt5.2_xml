
public class Uf {
String n;
String nom;

public Uf() {
	super();
}
public Uf(String n, String nom) {
	super();
	this.n = n;
	this.nom = nom;
}
public String getN() {
	return n;
}
public void setN(String n) {
	this.n = n;
}
public String getNom() {
	return nom;
}
public void setNom(String nom) {
	this.nom = nom;
}


}
